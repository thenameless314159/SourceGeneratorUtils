namespace SourceGeneratorUtils;

/// <summary>
/// Well known characters.
/// </summary>
public static class WellKnownChars
{
    /// <summary>
    /// Open and close brackets.
    /// </summary>
    public const char OpenBracket = '[', CloseBracket = ']';

    /// <summary>
    /// Open and close braces.
    /// </summary>
    public const char OpenBrace = '{', CloseBrace = '}';

    /// <summary>
    /// Open and close parenthesis.
    /// </summary>
    public const char OpenParen = '(', CloseParen = ')';

    /// <summary>
    /// Open and close angle brackets.
    /// </summary>
    public const char OpenAngle = '<', CloseAngle = '>';
}